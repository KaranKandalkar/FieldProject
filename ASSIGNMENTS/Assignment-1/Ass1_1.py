# Q1. Write a program to input two numbers and display their sum, difference, product, and division.

# Input two numbers
num1 = float(input("Enter first number: "))
num2 = float(input("Enter second number: "))

# Calculations
sum = num1 + num2
difference = num1 - num2
product = num1 * num2
division = num1 / num2 

# Display Results
print("\nSum =", sum)
print("Difference =", difference)
print("Product =", product)
print("Division =", division)
